import UIKit

let name = "Noah carpenter"

// Convert to lowercase
let lowercaseName = name.lowercased()

// Convert to uppercase
let uppercaseName = name.uppercased()

// Convert to normal casing (capitalize first letter of each word)
let normalCasedName = name.capitalized

// Print the results
print("Lowercased: \(lowercaseName)")
print("Uppercased: \(uppercaseName)")
print("Normal Casing: \(normalCasedName)")
print("No Changes \(name)")
